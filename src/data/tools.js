export const getToolsByCategory = (catId) => tools.filter((t) => t.category === catId);
export const getRelatedTools = (toolId) => {
  const tool = getToolById(toolId);
  if (!tool) return [];
  return tool.relatedTools.map(getToolById).filter(Boolean);
};
